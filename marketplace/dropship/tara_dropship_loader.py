# === Add Profit Calculations ===
def enrich_with_profit(products: List[Dict]) -> List[Dict]:
    for p in products:
        if p.get("cost", 0) > 0 and p.get("price", 0) > 0:
            margin = round(p["price"] - p["cost"], 2)
            profit_pct = round(100 * (margin / p["cost"]), 1)
            p.update({"margin": margin, "profit_pct": profit_pct})
        else:
            p.update({"margin": 0.0, "profit_pct": 0.0})
    return products

# === Add Checkout or Referral Link ===
def add_checkout_urls(products: List[Dict], base_url: str = "https://tara.shop/checkout") -> List[Dict]:
    for p in products:
        slug = p['name'].lower().replace(" ", "-").replace("(", "").replace(")", "")
        # Use referral_link if exists, else generate buy_now link
        p["buy_now"] = p.get("referral_link", f"{base_url}/{slug}")
    return products

# === Save to JSON File ===
def save_dropship_products(products: List[Dict]) -> None:
    PRODUCTS_FILE.parent.mkdir(parents=True, exist_ok=True)
    with open(PRODUCTS_FILE, 'w', encoding='utf-8') as f:
        json.dump(products, f, indent=2)
